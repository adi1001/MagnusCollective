public class Sensor {

    String sensorName;
    String sensorExpression;
    int sensorConstant;
    int sensorId;
    int alertId;

    public String getLogicalOperator() {
        return logicalOperator;
    }

    public void setLogicalOperator(String logicalOperator) {
        this.logicalOperator = logicalOperator;
    }

    String logicalOperator;

    public String getSensorName() {
        return sensorName;
    }
    public void setSensorName(String sensorName) {
        this.sensorName = sensorName;
    }
    public String getSensorExpression() {
        return sensorExpression;
    }
    public void setSensorExpression(String sensorExpression) {
        this.sensorExpression = sensorExpression;
    }
    public int getSensorConstant() {
        return sensorConstant;
    }
    public void setSensorConstant(int sensorConstant) {
        this.sensorConstant = sensorConstant;
    }
    public int getSensorId() {
        return sensorId;
    }
    public void setSensorId(int sensorId) {
        this.sensorId = sensorId;
    }
    public int getAlertId() {
        return sensorId;
    }
    public void setAlertId(int sensorId) {
        this.sensorId = sensorId;
    }
}

